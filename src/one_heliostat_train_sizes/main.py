def _build_mapping(split: str) -> list:
    return build_heliostat_data_mapping(
        benchmark_csv=cfg.BENCHMARK_CSV,
        calibration_properties_dir=cfg.CALIBRATION_PROPERTIES_DIR,
        flux_image_dir=cfg.FLUX_IMAGE_DIR,
        split=split,
    )


def _split_dir(split: str) -> pathlib.Path:
    return cfg.SYNTH_DATA_DIR / split


def _synth_split_has_heliostat(split: str, hid: str, n_samples: int) -> bool:
    hel_dir = _split_dir(split) / hid
    if not hel_dir.exists():
        return False
    return len(sorted(hel_dir.iterdir())) >= n_samples


def _run_reporting(
    results: dict,
    heliostat_ids: list,
    output_dir: pathlib.Path,
) -> None:
    for stage_name, filename, loss_label in [
        ("convergence_history_stage1.json", "convergence_stage1.png", "AlignmentLoss (rad²)"),
        ("convergence_history_stage2.json", "convergence_stage2.png", f"{results.get('loss_type', 'focal_spot')} Loss"),
    ]:
        history_file = output_dir / stage_name
        if history_file.exists():
            with open(history_file) as f:
                history = json.load(f)
            stage_label = "Stage 1 — AlignmentLoss" if "stage1" in stage_name else f"Stage 2 — {results.get('loss_type', 'focal_spot')}"
            plot_stage_convergence(
                history, output_dir,
                stage_name=stage_label,
                loss_label=loss_label,
                filename=filename,
            )


def _plot_convergence_overlay(
    train_sizes: list[int],
    run_dir: pathlib.Path,
    loss_type: str,
) -> None:
    """Overlay stage-2 val-loss convergence curves for all train sizes on one plot."""
    cmap = matplotlib.colormaps["plasma"].resampled(len(train_sizes))
    fig, ax = plt.subplots(figsize=(9, 5))
    fig.patch.set_facecolor("white")

    plotted = 0
    for i, n in enumerate(train_sizes):
        hist_file = run_dir / f"train_size_{n}" / "convergence_history_stage2.json"
        if not hist_file.exists():
            continue
        with open(hist_file) as f:
            history = json.load(f)
        epochs    = [e["epoch"]    for e in history]
        val_loss  = [e.get("eval_loss") for e in history]
        train_loss = [e.get("loss") for e in history]
        color = cmap(i)
        if any(v is not None for v in val_loss):
            vals = [v for v in val_loss if v is not None]
            eps  = [epochs[j] for j, v in enumerate(val_loss) if v is not None]
            ax.plot(eps, vals, color=color, linewidth=1.8, label=f"n={n}")
        elif train_loss:
            ax.plot(epochs, train_loss, color=color, linewidth=1.8, linestyle="--", label=f"n={n} (train)")
        plotted += 1

    if plotted == 0:
        plt.close(fig)
        return

    ax.set_xlabel("Epoch", fontsize=11)
    ax.set_ylabel("Stage-2 validation loss", fontsize=11)
    ax.set_title(f"Stage-2 convergence by train size — {loss_type}", fontsize=12)
    ax.legend(fontsize=9, ncol=2)
    ax.grid(True, alpha=0.35)
    fig.tight_layout()
    out = run_dir / "convergence_overlay.png"
    fig.savefig(out, dpi=150)
    plt.close(fig)


def _plot_summary(summary: dict, output_dir: pathlib.Path) -> None:
    """Plot post-training mrad (val and test) vs train size."""
    sizes     = summary["train_sizes"]
    val_mrad  = [summary["results"][str(n)]["post_training"]["val"]["mean_mrad"]  for n in sizes]
    test_mrad = [summary["results"][str(n)]["post_training"]["test"]["mean_mrad"] for n in sizes]
    pre_test  = summary["results"][str(sizes[0])]["pre_training"]["test"]["mean_mrad"]

    fig, ax = plt.subplots(figsize=(8, 5))
    fig.patch.set_facecolor("white")
    ax.plot(sizes, val_mrad,  marker="o", color="steelblue",  linewidth=2, label="post-training val")
    ax.plot(sizes, test_mrad, marker="s", color="darkorange", linewidth=2, label="post-training test")
    ax.axhline(pre_test, color="tomato", linestyle="--", linewidth=1.2,
               label=f"pre-training test ({pre_test:.2f} mrad)")
    ax.set_xlabel("Training samples")
    ax.set_ylabel("Mean focal-spot error (mrad)")
    ax.set_title(f"Recovery vs training samples — heliostat {summary['heliostat_id']}")
    ax.legend()
    ax.grid(True, alpha=0.4)
    fig.tight_layout()
    fig.savefig(output_dir / "summary.png", dpi=150)
    plt.close(fig)


def _write_summary_table(summary: dict, output_dir: pathlib.Path) -> None:
    """Write a human-readable metrics table for all train sizes to summary_table.txt."""
    sizes = summary["train_sizes"]
    hid   = summary["heliostat_id"]
    lines = [
        f"One-heliostat train-size sensitivity — {hid}",
        f"Dataset: {summary['dataset_type']}   Loss: {summary['loss_type']}",
        "=" * 70,
        f"{'Size':>5} | {'Pre-train val':>13} {'Pre-train tst':>13} | {'Post-train val':>14} {'Post-train tst':>14}",
        f"{'':>5}-+-{'':-<13}-{'':-<13}-+-{'':-<14}-{'':-<14}",
    ]
    for n in sizes:
        r     = summary["results"][str(n)]
        pre_v = r["pre_training"]["val"]["mean_mrad"]
        pre_t = r["pre_training"]["test"]["mean_mrad"]
        trn_v = r["post_training"]["val"]["mean_mrad"]
        trn_t = r["post_training"]["test"]["mean_mrad"]
        lines.append(
            f"{n:5d} | {pre_v:13.4f} {pre_t:13.4f} | {trn_v:14.4f} {trn_t:14.4f}"
        )
    lines.append("")
    (output_dir / "summary_table.txt").write_text("\n".join(lines))


