"""
One-heliostat train-size sensitivity experiment.

Sweeps over TRAIN_SIZES (e.g. [1, 5, 10, 20, 25, 50, 75, 100]) for a single
heliostat.  Each size runs a full train+eval cycle (same perturbed data, same
val/test split) so results are directly comparable.

Corrected pipeline: uses synthetic data pre-generated by
full_63_heli_kin_reconstruct/generate_dataset.py (data from perturbed scenario).
The KR starts from a clean scenario and must discover the perturbation values.
No separate data-generation step required.

Usage
-----
    python main.py
    python main.py --output-dir outputs/my_run
    python main.py --smoke-test
    python main.py --heliostat-id AA23
    python main.py --daic
"""
